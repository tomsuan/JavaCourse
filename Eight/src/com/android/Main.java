package com.android;

public class Main {

	public static void main(String[] args) {
		int i=4;
		i++;//adds one to i
		System.out.println("i is now "+i);
		int num=4;
		i+=num;//this is the same as i=i+num
		System.out.println("i is now "+i);
		
		int counter=0;
		//while loop, keyword is while
		while(counter<10){
			counter++;//this loop is 1 to 10
			System.out.println("counter is "+counter);
			
		}

	}

}
